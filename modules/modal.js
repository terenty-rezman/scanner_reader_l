export function showModal() {
  document.getElementById("scan-modal_id").classList.remove("hidden");
}

export function hideModal() {
  document.getElementById("scan-modal_id").classList.add("hidden");
}

export function showSentModal() {
  document.getElementById("sent-modal_id").classList.remove("hidden");
}

export function hideSentModal() {
  document.getElementById("sent-modal_id").classList.add("hidden");
}
